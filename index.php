Healthy Food for children entering their teenage years
</h1></div>
<span>        In adolescence, children need plenty of nutrients and calories during a growth spurt. Around age 10 to 11, girls generally experience a growth spurt. Boys generally experience one around the age of 12 to 13.
<br>
Recommendations include:<br>
In addition to supplying extra energy, foods should provide nutrients, not just ’empty calories’.
    The balance between takeaway and fast foods should include nutrient-rich foods such as wholegrains, legumes, nuts, vegetables, fish, and lean meats.<br>
    The consumption of milk, yoghurt, and cheese (mostly reduced fat) is important for calcium absorption – particularly for growing bones. Lower salt cheese is recommended.<br>
    The consumption of milk and milk products by adolescents should be encouraged.
</span>
