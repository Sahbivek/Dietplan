<div><h1>
Healthy Eating for young children
</h1></div>
<span>
Once a child has started eating solids, offer a variety of foods to ensure adequate nutrition. It is important to encourage young children to eat a variety of foods, as they are often picky about food. A child may need to try new foods again before they accept them. They may need to try them eight to fifteen times before they accept them.<br>

The food intake of children (spontaneously) varies with their growth patterns throughout childhood. The food needs of children vary greatly according to their age and activity level. An increasing number of nutrients, vitamins, and minerals are required by children as they grow.<br>
As children approach adolescence, they should be accumulating stores of nutrients to prepare for this rapid growth spurt. Whether food intake is appropriate is determined by weight gain and development.<br>

In addition to overweight and obesity, children with food sensitivities and tooth decay also face food-related problems.<br>

The following recommendations are made:<br>
Eat nutrient-poor, energy-dense snacks if your child is gaining excessive weight. Encourage your child to become more active. Reduce your child’s television watching.<br>
Brushing your teeth regularly and visiting the dentist can prevent tooth decay. Steer clear of sticky or acidic foods and beverages.<br>
Water is especially important for your child. Juices and soft drinks should be limited.
    Reduced-fat milk should not be given to children under two due to their high growth rate and increased energy needs.<br>
    Peanuts, shellfish, and cow’s milk are some foods that trigger allergic reactions. You should be especially careful if you have a family history of food allergies.
</span>
